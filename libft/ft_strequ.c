#include "../includes/libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int k;

	if (!s1 || !s2)
		return (0);
	i = 0;
	k = 0;
	while (s1[i] == s2[k] && s1[i] != '\0' && s2[k] != '\0')
	{
		i++;
		k++;
	}
	if (s1[i] == '\0' && s2[k] == '\0')
		return (1);
	else
		return (0);
}
